		<h2>Domothèque, qu'est-ce que c'est ?</h2>
		<p>
			Cela consiste à mettre en commun, pour les partager, des objets (petit électroménager, petit outillage, jeux de société, etc.) qui ne servent que rarement dans une année afin d’éviter l’achat de ce type de matériel à ceux qui ne les possèdent pas et une meilleure utilisation pour ceux qui en ont fait l’acquisition mais n’en font qu’un rare usage…
		<p>
		
